public class Objaw {

    private int id;
    private String nazwa;

    public Objaw(int id, String nazwa) {
        this.id = id;
        this.nazwa = nazwa;
    }

    public int getId() {
        return id;
    }

    public String getNazwa() {
        return nazwa;
    }
}
